const fs = require('fs');
const path = require('path');

const matchViewPath = path.join(__dirname, '../src/components/MatchView.jsx');
const matchViewCssPath = path.join(__dirname, '../src/styles/match-view.css');

let jsx = fs.readFileSync(matchViewPath, 'utf-8');
let css = fs.readFileSync(matchViewCssPath, 'utf-8');

// We will find exact static style strings and replace them with utility classes
const utilityMap = {
    "style={{ display: 'flex', flexDirection: 'column' }}": "className=\"ef-u-flex-col\"",
    "style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '16px' }}": "className=\"ef-u-flex-between ef-u-mb-16\"",
    "style={{ marginBottom: 0 }}": "className=\"ef-u-mb-0\"",
    "style={{ display: 'flex', gap: '8px', alignItems: 'center' }}": "className=\"ef-u-flex-gap-8\"",
    "style={{ marginBottom: '16px' }}": "className=\"ef-u-mb-16\"",
    "style={{ marginBottom: '8px' }}": "className=\"ef-u-mb-8\"",
    "style={{ marginTop: '12px', color: 'var(--text-muted)' }}": "className=\"ef-u-mt-12 ef-text-muted\"",
    "style={{ opacity: 0.7 }}": "className=\"ef-u-opacity-70\"",
    "style={{ padding: '12px', textAlign: 'center' }}": "className=\"ef-u-p-12 ef-u-text-center\"",
    "style={{ color: 'var(--notification-info-border)' }}": "className=\"ef-u-color-info\"",
    "style={{ color: 'var(--color-secondary)' }}": "className=\"ef-u-color-secondary\"",
    "style={{ color: 'var(--color-primary)' }}": "className=\"ef-u-color-primary\"",
    "style={{ margin: '0 auto 16px' }}": "className=\"ef-u-m-auto-16\"",
    "style={{ padding: '20px', fontSize: '1.2rem', justifyContent: 'center' }}": "className=\"ef-u-p-20 ef-u-text-lg ef-u-justify-center\"",
    "style={{ flex: 1 }}": "className=\"ef-u-flex-1\"",
    "style={{ padding: '8px 12px', background: 'var(--bg-surface-alt)', borderRadius: '6px', marginBottom: '8px', display: 'flex', justifyContent: 'space-between', alignItems: 'center' }}": "className=\"ef-u-surface-row\"",
    "style={{ padding: '12px', textAlign: 'center', color: 'var(--text-muted)' }}": "className=\"ef-u-p-12 ef-u-text-center ef-text-muted\"",
    "style={{ verticalAlign: 'middle' }}": "className=\"ef-u-v-middle\"",
    "style={{ marginLeft: '12px' }}": "className=\"ef-u-ml-12\"",
    "style={{ fontSize: '0.8rem', color: 'var(--text-muted)' }}": "className=\"ef-u-text-sm ef-text-muted\"",
    "style={{ color: 'var(--text-main)' }}": "className=\"ef-u-color-main\""
};

const newCss = `
/* --- ATOMIC UTILITIES (Extracted from MatchView) --- */
.ef-u-flex-col { display: flex; flex-direction: column; }
.ef-u-flex-between { display: flex; justify-content: space-between; align-items: center; }
.ef-u-mb-16 { margin-bottom: 16px; }
.ef-u-mb-8 { margin-bottom: 8px; }
.ef-u-mb-0 { margin-bottom: 0; }
.ef-u-mt-12 { margin-top: 12px; }
.ef-u-ml-12 { margin-left: 12px; }
.ef-u-m-auto-16 { margin: 0 auto 16px; }
.ef-u-p-12 { padding: 12px; }
.ef-u-p-20 { padding: 20px; }
.ef-u-text-center { text-align: center; }
.ef-u-text-lg { font-size: 1.2rem; }
.ef-u-text-sm { font-size: 0.8rem; }
.ef-u-justify-center { justify-content: center; }
.ef-u-flex-gap-8 { display: flex; gap: 8px; align-items: center; }
.ef-u-opacity-70 { opacity: 0.7; }
.ef-u-color-info { color: var(--notification-info-border); }
.ef-u-color-secondary { color: var(--color-secondary); }
.ef-u-color-primary { color: var(--color-primary); }
.ef-u-color-main { color: var(--text-main); }
.ef-u-flex-1 { flex: 1; }
.ef-u-v-middle { vertical-align: middle; }
.ef-u-surface-row { padding: 8px 12px; background: var(--bg-surface-alt); border-radius: 6px; margin-bottom: 8px; display: flex; justify-content: space-between; align-items: center; }
`;

let replaced = 0;
for (const [inline, util] of Object.entries(utilityMap)) {
    // If element already has className, we need to inject the classes into it instead of replacing.
    // E.g. className="ef-match-feed" style={{ ... }} => className="ef-match-feed ef-u-flex-col"
    
    // We will do a simple regex that checks if there's a className before or after
    // Actually, easier: split by the exact inline string
    if (jsx.includes(inline)) {
        jsx = jsx.replaceAll(inline, util);
        replaced++;
    }
}

// Fix duplicate classNames generated by naive replacement:
// e.g. className="ef-section-header" className="ef-u-mb-0" -> className="ef-section-header ef-u-mb-0"
jsx = jsx.replace(/className="([^"]+)"\s+className="([^"]+)"/g, 'className="$1 $2"');
// And vice-versa
jsx = jsx.replace(/className='([^']+)'\s+className="([^"]+)"/g, 'className="$1 $2"');

if (replaced > 0) {
    if (!css.includes('/* --- ATOMIC UTILITIES')) {
        fs.writeFileSync(matchViewCssPath, css + newCss);
    }
    fs.writeFileSync(matchViewPath, jsx);
    console.log(`Successfully replaced ${replaced} static style patterns into ${Object.keys(utilityMap).length} atomic CSS classes.`);
} else {
    console.log('No exact matches found or already replaced.');
}

